package com.example.learn_spring_framework.game;

public class MarioGame {
	public void up()
	{
		System.out.println("jump");
	}
	public void down()
	{
		System.out.println("GO into a hole");
	}
	public void left()
	{
		System.out.println("go back");
	}
	public void right()
	{
		System.out.println("go front");
	}
}